package by.it_academy.les03.main;

public class LinearApps1_01 {

	public static void main(String[] args) {

		double x = 16;
		double y = 7;
		double sum, dif1, dif2, comp, quot1, quot2;

		sum = x + y;
		dif1 = x - y;
		dif2 = y - x;
		comp = x * y;
		quot1 = x / y;
		quot2 = y / x;
		System.out.println("sum: " + x + " + " + y + " = " + sum);
		System.out.println("dif1: " + x + " - " + y + " = " + dif1);
		System.out.println("dif2: " + y + " - " + x + " = " + dif2);
		System.out.println("comp: " + x + " * " + y + " = " + comp);
		System.out.println("quot1: " + x + " / " + y + " = " + quot1);
		System.out.println("quot2: " + y + " / " + x + " = " + quot2);

	}

}
